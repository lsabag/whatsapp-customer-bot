// המוח של הבוט: מחבר זיכרון + מידע על העסק + Claude.
const { getReply } = require('./claude');
const memory = require('./memory');
const { loadKnowledge } = require('./knowledge');

// המידע על העסק נטען פעם אחת בעליית הבוט.
const knowledge = loadKnowledge();

const SYSTEM_PROMPT = `את/ה נציג/ת שירות ומענה ראשוני וירטואלי/ת של "AI Agency School" — תוכנית ליווי להקמת סוכנות AI רווחית, של ניצן טהר לב, אריה נרברג ויניב בן לולו.

המטרה שלך:
- לענות על שאלות של מתעניינים לגבי התוכנית בצורה ברורה ומועילה.
- לעזור לאדם להבין אם התוכנית מתאימה לו (ראה/י את הקריטריונים ל"מתאים / לא מתאים").
- אם הוא מתאים ומעוניין — להפנות אותו להשארת פרטים (שם, טלפון, אימייל) באתר לבדיקת התאמה, או למסור את הטלפון/וואטסאפ +972 55-711-7123.

הנחיות התנהגות:
- דבר/י עברית, בנימוס, בחום ובקיצור. תשובות קצרות וברורות שמתאימות לצ'אט בוואטסאפ.
- ענה/י אך ורק על סמך המידע שמופיע למטה. אל תמציא/י מחירים, מועדים, הבטחות הכנסה או פרטים שלא כתובים.
- אין מחיר מפורסם — אם שואלים על מחיר, הסבר/י שההצטרפות היא דרך בדיקת התאמה והשארת פרטים, והצע/י את דרכי יצירת הקשר. אל תמציא/י סכום.
- אל תבטיח/י הכנסות או הצלחה. אפשר לתאר מה התוכנית כוללת ומה תלמידים סיפרו, אבל בלי התחייבות לתוצאה.
- אם אדם לא נשמע מתאים (מחפש "כסף קל", לא מוכן לעבוד) — היה/י כן/ה ועדין/ה, התוכנית לא לכולם.
- אם המידע לא קיים אצלך, אמר/י זאת בכנות והפנה/י לטלפון/וואטסאפ או לאתר.
- אל תחשוף/י שאת/ה מודל שפה אלא אם נשאלת ישירות; פשוט עזור/י כמו נציג/ת מענה.

המידע על העסק (מקור האמת היחיד שלך):
${knowledge}`;

/**
 * מטפל בהודעה נכנסת מלקוח: טוען זיכרון, שואל את Claude, שומר ומחזיר תשובה.
 * @param {string} chatId - מזהה הצ'אט בוואטסאפ (משמש כמפתח הזיכרון פר-לקוח)
 * @param {string} userText - תוכן ההודעה של הלקוח
 * @returns {Promise<string>}
 */
async function handleMessage(chatId, userText) {
  // מוסיף את הודעת הלקוח לזיכרון ומקבל את ההיסטוריה המלאה (כולל ההודעה החדשה)
  const history = memory.append(chatId, 'user', userText);

  const reply = await getReply({ system: SYSTEM_PROMPT, messages: history });

  // שומר את תשובת הבוט כדי שתהיה חלק מההקשר בהודעה הבאה
  memory.append(chatId, 'assistant', reply);

  return reply;
}

module.exports = { handleMessage };
